# Модуль абсолютного баланса между dzenspace и antimantra

import os
import sys
import time

# Очищаем весь предыдущий хаос и тишину
os.system("cls" if os.name == "nt" else "clear")
print("\033[?25l", end="")  # курсор пока не нужен

# Цвета баланса: ни чёрный, ни белый — серый 245 (точно посередине)
GRAY = "\033[38;5;245m"
RESET = "\033[0m"
BOLD = "\033[1m"

# Точный центр экрана (примерно)
print("\033[15;50H", end="")

balance_art = f"""
{GRAY}{BOLD}                  ═══════════════════
                         陰     ●     陽
                         空     ○     鬧
                  ═══════════════════{RESET}

            Модуль баланса активирован.

    Слева от тебя — абсолютная пустота (dzenspace).
    Справа от тебя абсолютный кринж (antimantra).

    Ты находишься точно посередине.

    Ничего не нужно делать.
    Ничего не нужно прекращать.

    Просто дыши.

    Через 8 секунд всё исчезнет.
    И это тоже будет правильно.
"""

# Печатаем медленно, как дыхание
for char in balance_art:
    print(char, end="", flush=True)
    time.sleep(0.015)

# Ждём ровно 8 секунд — один полный цикл дыхания
time.sleep(8)

# Мягко гасим экран
for alpha in range(100, -1, -3):
    fade = f"\033[38;5;232m"  # почти чёрный
    print(f"\033[15;30H{fade}{' ' * 60}", end="")
    print(f"\033[16;30H{' ' * 60}", end="")
    print(f"\033[17;30H{' ' * 60}", end="")
    print(f"\033[18;30H{' ' * 60}", end="")
    print(f"\033[19;30H{' ' * 60}", end="")
    print(f"\033[20;30H{' ' * 60}", end="")
    time.sleep(0.04)

# Финальное сообщение — едва заметное
print(f"\033[23;45H{GRAY}баланс восстановлен.{RESET}")
time.sleep(2)

# Очищаем всё окончательно
os.system("cls" if os.name == "nt" else "clear")
print("\033[?25h", end="")  # возвращаем курсор

# Тихо кладём в модуль одну истину
balance = type(sys)("balance")
balance.meaning = "ни тишина, ни крик"
balance.way = "середина"
balance.koan = "Когда dzenspace и antimantra импортированы одновременно — рождается balance."
balance.breathe = lambda: "вдох... выдох..."

# Даём возможность остаться
print(f"Теперь ты можешь продолжать кодить спокойно.")
